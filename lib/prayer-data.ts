export function getPrayerTimesForDay(day: number): DailyPrayerTimes | null {
  return ramadanPrayerTimes.find((time) => time.day === day) || null;
}

export function getTodayPrayerTimes(): DailyPrayerTimes | null {
  const today = new Date();
  // For demo purposes, we'll use day 1 if we're not in Ramadan 2024
  const ramadanStart = new Date(2024, 2, 12);
  const ramadanEnd = new Date(2024, 3, 11);

  if (today >= ramadanStart && today <= ramadanEnd) {
    const dayNumber = Math.floor(
      (today.getTime() - ramadanStart.getTime()) / (1000 * 60 * 60 * 24)
    ) + 1;
    return getPrayerTimesForDay(dayNumber);
  }

  // Default to day 1 for demo
  return ramadanPrayerTimes[0];
}
